#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}

/*
esta funcion aplica una funcion a cada uno de los indices de
la cadena s, no devuelve nada solo aplica la funcion descrita como parametro
*/